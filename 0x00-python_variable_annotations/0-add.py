#!/usr/bin/python3
'''Type Annotations for Function Parameters and Return Values'''
def add(a: float, b: float) -> float:
    """
    Adds two floating-point numbers and returns the result.

    Parameters:
        a (float): The first floating-point number to be added.
        b (float): The second floating-point number to be added.
    
    Return:
        float: the sum of the two input floating-point numbers.
    """
    return a + b
